package nivelIntermediario.construtores;

public class Hokage {
    String nome;
    int idade;
    boolean vivoOuMorto;

    public Hokage() {
    }

    public Hokage(String nome, int idade, boolean vivoOuMorto) {
        this.nome = nome;
        this.idade = idade;
        this.vivoOuMorto = vivoOuMorto;
    }
}
